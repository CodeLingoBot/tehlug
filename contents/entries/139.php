<div class="subject">
پی-تو-پی کارنسی
</div>

<div class="date">
1390/03/17
</div>

<div class="body">
این جلسه در ادامه‌ی سری جدید و منظم برنامه‌های تهران لاگ، مورخ ۱۷ خرداد ماه سال ۱۳۹۰ در فرهنگسرای فن آوری اطلاعات واقع در تقاطع خیابان کارگر و جمهوری برگزار خواهد شد. 
در این جلسه آقای شاهین وارسته در مورد پی-تو-پی کارنسی صحبت خواهند کرد.
