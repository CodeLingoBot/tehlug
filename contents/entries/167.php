<div class="subject">
مپ‌ردوس، هدوپ و پکت اینجکشن
</div>

<div class="date">
1391/11/24
</div>

<div class="body">
در این جلسه ابتدا آقای مجید عظیمی در مورد مدل مپ‌ردوس و تکنولوژی هدوپ به ارائه‌ی مطلب خواهند پرداخت. سپس در ادامه آقای اشکان قاسمی در مورد پکت اینجکشن و ‌‌محل استفاده‌ی این متد در تکنولوژی‌های مخربی که دسترسی به محتوای آزاد در سطح اینترنت را محدود میسازند به ارائه مطلب خواهند پرداخت.
</div>
