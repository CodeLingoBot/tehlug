<div class="body">
جلسه ۲۱۵ گروه کاربران لینوکس تهران روز چهارشنبه ۲۰ خرداد  از ساعت ۱۸ الی ۲۰  در محل نظام صنفی رایانه ای استان تهران برگزار می‌شود.
موضوع جلسه‌ی آینده ما هنوز نامشخص است. در صورت تمایل به ارائه مطلب یا مدیریت بحث آزاد موضوع را با لیست پستی گروه در میان بگذارید.
