در این جلسه نیز همانند جلسه‌ قبلی در کنار ارائه‌های فنی، کارگاهی جداگانه با موضوع آشنایی با مفاهیم نرم‌افزار آزاد و نصب و استفاده از گنو/لینوکس خواهیم داشت که توسط جمعی از کاربران برگزار خواهد شد.
<br />
<br />
<br />
<br />
ارائهٔ فنّی نخست توسط آقای احسان یوسفی با موضوع Reactive programming with Scala and AKKA خواهد بود.
<br />
<br />

ارائهٔ فنّی دوم توسط آقایان دانیال بهزادی و امیرحسین گودرزی با موضوع LXD خواهد بود.
<br />
<br />

سپس در یک ساعت میانی به پذیرایی، شبکه‌سازی و گفت‌و‌گو بین کاربران خواهیم پرداخت.
<br />
<br />

