این جلسه در روز چهار‌شنبه مورخ ۱۴ تیرماه ۱۳۹۱ از ساعت ۱۸ الی ۲۰ در فضای آزاد پارک لاله برگزار می‌شود.<br />در این جلسه آقای افشین مهربانی به توضیح در مورد twitter bootstrap (یک Framework/Tools برای طراحی User Interfaceهای بهتر با کارایی و زیبایی بیش‌تر) خواهند پرداخت.<br />از مسائل مهمی که در حاشیه این موضوع مطرح می‌شه می‌شود به طراحی User Interface برای موبایل و تبلت، MVVM Pattern، سیستم‌های Grid و Integration در User Interface اشاره کنم.<br /><br />

<b>جلسه دقیقا در کجا برگزار می‌شود؟</b>  ما ابتدا در کنار مجسمه ابوریحان بیرونی در ضلع جنوب غربی پارک لاله (واقع در بلوار کشاورز) جمع خواهیم شد و سپس در همان نزدیکی جلسه را برگزار خواهیم کرد. در صورتی که نتوانستید ما را پیدا کنید کافی است با شماره ۰۹۳۵۸۸۶۳۵۹۰ (آقای محمد افاضاتی) تماس حاصل فرمایید<br /><br />
